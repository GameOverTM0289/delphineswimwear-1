export type ShippingZone = 'AL' | 'EU' | 'NA' | 'WORLD';

export interface Country {
  /** ISO 3166-1 alpha-2 */
  code: string;
  name: string;
  /** International dial code without the leading +. */
  dial: string;
  /** Shipping zone — used by the rate calculator. */
  zone: ShippingZone;
  /** VAT metadata only. Customer-facing VAT is currently not itemized. */
  vat: number;
  /** Currency hint (informational only — we charge in EUR). */
  currency?: string;
}

